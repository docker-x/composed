func TestEmit_MinimalService(t *testing.T) {
	f := NewFile()
	f.Project = "test"
	f.Services["web"] = NewService(testImageNginx)

	out, err := Emit(f)
	if err != nil {
		t.Fatalf(errFmtEmit, err)
	}

	if !strings.Contains(out, "name: test") {
		t.Error("missing project name")
	}
	if !strings.Contains(out, "web:") {
		t.Error("missing service name")
	}
	if !strings.Contains(out, "image: nginx:latest") {
		t.Error("missing image")
	}
}

