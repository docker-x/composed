func TestEmit_FullService(t *testing.T) {
	f := NewFile()
	f.Project = "test"

	svc := NewService("myapp:latest")
	svc.Entrypoint = []string{"/bin/sh", "-c"}
	svc.Command = []string{"serve", "--port=8080"}
	svc.Environment["PORT"] = "8080"
	svc.Environment["DEBUG"] = "true"
	svc.Ports = []string{"8080:8080", "9090:9090"}
	svc.Volumes = []string{"data:/data"}
	svc.Tmpfs = []string{"/tmp"}
	svc.ShmSize = "256m"
	svc.Restart = "unless-stopped"
	svc.Labels["team"] = "backend"
	svc.Profiles = []string{"dev"}
	svc.DependsOn["postgres"] = DependsOnCondition{Condition: "service_healthy"}
	svc.Healthcheck = &Healthcheck{
		Test:        []string{"CMD", "curl", "-f", "http://localhost:8080/health"},
		Interval:    "10s",
		Timeout:     "5s",
		Retries:     3,
		StartPeriod: "30s",
	}
	replicas := 2
	svc.Deploy = &Deploy{
		Replicas: &replicas,
		Resources: &Resources{
			Limits:       &ResourceSpec{CPUs: "1.00", Memory: "512M"},
			Reservations: &ResourceSpec{CPUs: "0.25", Memory: "128M"},
		},
	}
	svc.Configs = []ServiceConfig{{Source: "my-config", Target: "/etc/app/config.yaml"}}

	f.Services["app"] = svc
	f.Volumes["data"] = &Volume{}
	f.Networks["frontend"] = &Network{}
	f.Configs["my-config"] = &Config{Content: "key: value"}

	out, err := Emit(f)
	if err != nil {
		t.Fatalf(errFmtEmit, err)
	}

	checks := []string{
		"name: test",
		"app:",
		"image: myapp:latest",
		"entrypoint:",
		"/bin/sh",
		"command:",
		"serve",
		"environment:",
		"PORT: 8080",
		"ports:",
		"8080:8080",
		"volumes:",
		"data:/data",
		"tmpfs:",
		"/tmp",
		"shm_size: 256m",
		"depends_on:",
		"postgres:",
		"condition: service_healthy",
		"healthcheck:",
		"test:",
		"CMD",
		"interval: 10s",
		"timeout: 5s",
		"retries: 3",
		"start_period: 30s",
		"deploy:",
		"replicas: 2",
		"resources:",
		"limits:",
		"cpus: 1.00",
		"memory: 512M",
		"reservations:",
		"restart: unless-stopped",
		"profiles:",
		"dev",
		"labels:",
		"team: backend",
		"volumes:",
		"data:",
		"networks:",
		"frontend:",
		"configs:",
		"my-config:",
		"content:",
		"key: value",
	}

	for _, check := range checks {
		if !strings.Contains(out, check) {
			t.Errorf("missing %q in output:\n%s", check, out)
		}
	}
}

func TestEmit_DeterministicOrder(t *testing.T) {
	f := NewFile()
	f.Services["zebra"] = NewService("zebra:latest")
	f.Services["alpha"] = NewService("alpha:latest")
	f.Services["middle"] = NewService("middle:latest")

	out1, _ := Emit(f)
	out2, _ := Emit(f)

	if out1 != out2 {
		t.Error("Emit should produce deterministic output")
	}

	// alpha should come before middle, middle before zebra
	alphaIdx := strings.Index(out1, "alpha:")
	middleIdx := strings.Index(out1, "middle:")
	zebraIdx := strings.Index(out1, "zebra:")

	if alphaIdx > middleIdx || middleIdx > zebraIdx {
		t.Errorf("services not in alphabetical order: alpha=%d, middle=%d, zebra=%d",
			alphaIdx, middleIdx, zebraIdx)
	}
}

